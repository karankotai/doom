export interface CountryShape {
  id: string;
  name: string;
  path: string;
}

// Equirectangular projection: viewBox "0 0 1000 500"
// Conversion: x = (lon + 180) * (1000/360)   y = (90 - lat) * (500/180)
// Helper reference:
//   (0°lon, 0°lat) => (500, 250)
//   London (0°, 51°) => (500, 108)
//   New York (-74°, 41°) => (294, 136)
//   Tokyo (140°, 36°) => (889, 150)
//   São Paulo (-47°, -24°) => (369, 317)
//   Sydney (151°, -34°) => (919, 344)

function ll(lon: number, lat: number): string {
  const x = ((lon + 180) / 360) * 1000;
  const y = ((90 - lat) / 180) * 500;
  return `${Math.round(x * 10) / 10} ${Math.round(y * 10) / 10}`;
}

function poly(coords: [number, number][]): string {
  return coords
    .map((c, i) => `${i === 0 ? "M" : "L"} ${ll(c[0], c[1])}`)
    .join(" ") + " Z";
}

